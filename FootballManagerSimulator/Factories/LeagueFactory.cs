using FootballManagerSimulator.Interfaces;
using FootballManagerSimulator.Structures;
using Newtonsoft.Json.Linq;

namespace FootballManagerSimulator.Factories;

public class LeagueFactory : ICompetitionFactory
{
    public string CompetitionType => "League";

    private readonly IUtils Utils;

    public LeagueFactory(IUtils utils)
    {
        Utils = utils;
    }

    public ICompetition CreateCompetition(Competition competition)
    {
        var teams = Utils.GetResource<IEnumerable<Club>>("teams.json").Where(p => p.CompetitionID == competition.ID);
        var league = new League()
        {
            ID = competition.ID,
            Name = competition.Name,
            CompetitionType = CompetitionType,
            Teams = teams
        };
        league.Fixtures = GenerateNextRoundOfFixtures(teams.ToList(), league);
        return league;
    }

    public ICompetition Deserialise(JObject data)
    {
        var serialisableCompetitionModel = data.ToObject<SerialisableCompetitionModel>();

        var league = new League()
        {
            ID = serialisableCompetitionModel.ID,
            CompetitionType = CompetitionType,
            Teams = Utils.GetResource<IEnumerable<Club>>("teams.json").Where(p => p.CompetitionID == serialisableCompetitionModel.ID),
            Name = serialisableCompetitionModel.Name
        };
        league.Fixtures = serialisableCompetitionModel.Fixtures.Select(p => new Fixture
        {
            AwayTeam = Utils.GetTeam(p.AwayTeamID),
            WeekNumber = p.WeekNumber,
            Concluded = p.Concluded,
            Date = p.Date,
            ID = p.ID,
            GoalsAway = p.GoalsAway,
            GoalsHome = p.GoalsHome,
            HomeTeam = Utils.GetTeam(p.HomeTeamID),
            Competition = league
        }).ToList();
        return league;
    }

    //Generated by Chat GPT
    public List<Fixture> GenerateNextRoundOfFixtures(List<Club> teams, League league)
    {
        var output = new List<Fixture>();

        int numRounds = teams.Count - 1;
        int halfSize = teams.Count / 2;

        var teamIndices = new List<Club>(teams);

        teamIndices.RemoveAt(0);

        var teamIdxSize = teamIndices.Count;

        var date = new DateOnly(2022, 07, 02);

        for (int round = 0; round < numRounds; round++)
        {
            int teamIdx = round % teamIdxSize;

            output.Add(new Fixture
            {
                HomeTeam = Utils.GetTeam(teams[0].ID),
                AwayTeam = Utils.GetTeam(teamIndices[teamIdx].ID),
                WeekNumber = round + 1,
                Date = date,
                Competition = league
            });

            for (int idx = 1; idx < halfSize; idx++)
            {
                int firstTeamIdx = (round + idx) % teamIdxSize;
                int secondTeamIdx = (round + teamIdxSize - idx) % teamIdxSize;

                output.Add(new Fixture
                {
                    HomeTeam = Utils.GetTeam(teamIndices[firstTeamIdx].ID),
                    AwayTeam = Utils.GetTeam(teamIndices[secondTeamIdx].ID),
                    WeekNumber = round + 1,
                    Date = date,
                    Competition = league
                });
            }

            date = date.AddDays(7);
        }

        for (int round = 0; round < numRounds; round++)
        {
            int teamIdx = round % teamIdxSize;

            output.Add(new Fixture
            {
                HomeTeam = Utils.GetTeam(teamIndices[teamIdx].ID),
                AwayTeam = Utils.GetTeam(teams[0].ID),
                WeekNumber = numRounds + round + 1,
                Date = date,
                Competition = league
            });

            for (int idx = 1; idx < halfSize; idx++)
            {
                int firstTeamIdx = (round + idx) % teamIdxSize;
                int secondTeamIdx = (round + teamIdxSize - idx) % teamIdxSize;

                output.Add(new Fixture
                {
                    HomeTeam = Utils.GetTeam(teamIndices[secondTeamIdx].ID),
                    AwayTeam = Utils.GetTeam(teamIndices[firstTeamIdx].ID),
                    WeekNumber = numRounds + round + 1,
                    Date = date,
                    Competition = league
                });
            }
        }

        return output;
    }
}
